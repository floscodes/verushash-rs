use aes::cipher::{BlockEncrypt, KeyInit};
use hex_literal::hex;

pub fn haraka512_5(out: &mut [u8;32], input: &[u8;64]) {
    let mut state = [
        input[0..16].try_into().unwrap(),
        input[16..32].try_into().unwrap(),
        input[32..48].try_into().unwrap(),
        input[48..64].try_into().unwrap(),
    ];

    for round in 0..5 {
        // 2 × AES per Haraka round
        for i in 0..4 {
            aes_round(&mut state[i], &HARAKA512_RC[round*8 + i*2]);
            aes_round(&mut state[i], &HARAKA512_RC[round*8 + i*2 + 1]);
        }
        mix512(&mut state);
    }

    // feed‑forward with original input
    for i in 0..4 {
        for j in 0..16 {
            state[i][j] ^= input[i*16 + j];
        }
    }

    // take top 256 bits
    out[..16].copy_from_slice(&state[0]);
    out[16..].copy_from_slice(&state[1]);
}


fn aes_round(state: &mut [u8; 16], key: &[u8; 16]) {
    // AES‑128 single round with key = RC
    let cipher = aes::Aes128::new_from_slice(key).unwrap();
    let mut block = (*state).into();
    cipher.encrypt_block(&mut block);
    *state = block.into();
}

fn mix512(state: &mut [[u8;16]; 4]) {
    let t = *state;

    state[0] = [
        t[0][12], t[2][12], t[1][12], t[3][12],
        t[3][13], t[1][13], t[2][13], t[0][13],
        t[0][14], t[2][14], t[1][14], t[3][14],
        t[3][15], t[1][15], t[2][15], t[0][15],
    ];
    state[1] = [
        t[1][0], t[3][0], t[2][0], t[0][0],
        t[0][1], t[2][1], t[3][1], t[1][1],
        t[1][2], t[3][2], t[2][2], t[0][2],
        t[0][3], t[2][3], t[3][3], t[1][3],
    ];
    state[2] = [
        t[2][4], t[0][4], t[3][4], t[1][4],
        t[1][5], t[3][5], t[0][5], t[2][5],
        t[2][6], t[0][6], t[3][6], t[1][6],
        t[1][7], t[3][7], t[0][7], t[2][7],
    ];
    state[3] = [
        t[3][8], t[1][8], t[0][8], t[2][8],
        t[2][9], t[0][9], t[1][9], t[3][9],
        t[3][10],t[1][10],t[0][10],t[2][10],
        t[2][11],t[0][11],t[1][11],t[3][11],
    ];
}


